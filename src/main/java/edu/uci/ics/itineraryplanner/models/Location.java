package edu.uci.ics.itineraryplanner.models;

public class Location {
	
	private double lat;
	private double lng;
	public double getLat() {
		return lat;
	}
	public void setLat(long lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(long lng) {
		this.lng = lng;
	}
	
}
